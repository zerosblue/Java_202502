package ch04.ex01;

import java.util.Scanner;

public class ForExam04_배열7 {

	public static void main(String[] args) {
		
		for(int i=1; i<=9; i++) {
			System.out.printf("**********%d단**************\n",i);
			for(int j=1; j<=9; j++) {
				System.out.printf("%dx%d = %d\n", i,j,i*j);
			}
			System.out.println();
		}
		

		}

		
	}





package ch02.sec01;

public class charexample {

	public static void main(String[] args) {
		
		char c1 = 'A';
		System.out.println(c1);
		
		char c2 = 65;
		System.out.println(c2);
		
		char c3 = '가';
		System.out.println(c3);

		char c4 = 44032;
		System.out.println(c4);
		
		
	   
		// Ctrl + alt + 아래방향키
		/*Windows 기준
		Ctrl + Alt + ↑(위쪽 화살표) → 현재 줄을 위쪽으로 복사
		Ctrl + Alt + ↓(아래쪽 화살표) → 현재 줄을 아래쪽으로 복사

		Mac 기준
		(Command) + ⌥(Option) + ↑(위쪽 화살표) → 현재 줄을 위쪽으로 복사
	    (Command) + ⌥(Option) + ↓(아래쪽 화살표) → 현재 줄을 아래쪽으로 복사 */

	}

}

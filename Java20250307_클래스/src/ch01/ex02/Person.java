package ch01.ex02;
//클래스 명은 대문자
public class Person {
	//필드 인스턴스 변수 
	
	String name;
	String phone;
	int age;

	
	void 나이출력(String age) {
		System.out.println("나이 :" + age);
		
	}
	void 전화번호출력(String phone) {
		System.out.println("전화번호 : " + phone);
		
	}
	void 이름출력(String name) {
		System.out.println("이름 :"+ name );
		
	}

}
